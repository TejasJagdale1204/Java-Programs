public class matrix_2_by_2 {
    public static void main (String [] args) {
        int arr2d[][] = { {2,3},{6,7} };
        for(int i=0;i<2;i++){
            for (int j=0;j<2;j++) {
                System.out.print(arr2d[i][j] + " ");
            }
            System.out.println();
        }        
    }
}
