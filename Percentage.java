public class Percentage {
    public static void main (String [] args) {
        int phy = 70;
        int chem = 85;
        int maths = 91;
        int eng = 58;
        int cs = 186;

        float percentage;
        percentage = (phy+chem+maths+eng+cs)/6F;
        System.out.println("Final Result in percentage : "+percentage);
    }
}
