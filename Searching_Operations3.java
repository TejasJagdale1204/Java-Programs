public class Searching_Operations3 {
    public static void main(String[] args) {
        String s1 = "java hari krishna";

        System.out.println(s1.endsWith("hari"));
        System.out.println(s1.endsWith("Java"));
        System.out.println(s1.endsWith("hari kri"));
        System.out.println(s1.endsWith("java"));
        System.out.println(s1.endsWith("j"));
        System.out.println(s1.endsWith("java h"));
        System.out.println(s1.endsWith("krishna"));
        System.out.println(s1.endsWith("na"));
        System.out.println(s1.endsWith("shna"));
    }
}
