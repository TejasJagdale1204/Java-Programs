public class Searching_Operations2 {
    public static void main(String[] args) {
        String s1 = "java hari krishna";

        System.out.println(s1.startsWith("hari"));
        System.out.println(s1.startsWith("kri"));
        System.out.println(s1.startsWith("avaj"));
        System.out.println(s1.startsWith("Java"));
        System.out.println(s1.startsWith("hari kri"));
        System.out.println(s1.startsWith("java"));
        System.out.println(s1.startsWith("j"));
        System.out.println(s1.startsWith("java h"));
    }
}
