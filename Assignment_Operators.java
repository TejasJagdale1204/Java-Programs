class Assignment_Operators {
    public static void main (String [] args) {
        int a = 7;
        int b = 5;
        int c = 4;
        int d = 11;
        int e = 15;

        System.out.println(a+=3);
        System.out.println(b-=1);
        System.out.println(c*=5);
        System.out.println(d/=2);
        System.out.println(e%=4);
    }
}